function makeGlb() {
  const json = Buffer.from(GLTF_JSON, "utf8");
  const padLen = (4 - (json.length % 4)) % 4;
  const chunk0 = Buffer.concat([json, Buffer.alloc(padLen, 0x20)]);
  const totalLen = 12 + 8 + chunk0.length;
  const hdr = Buffer.alloc(12);
  hdr.writeUInt32LE(0x46546C67, 0);
  hdr.writeUInt32LE(2, 4);
  hdr.writeUInt32LE(totalLen, 8);
  const c0hdr = Buffer.alloc(8);
  c0hdr.writeUInt32LE(chunk0.length, 0);
  c0hdr.writeUInt32LE(0x4E4F534A, 4);
  return Buffer.concat([hdr, c0hdr, chunk0]);
}

function makeStlBinary(nFaces) {
  const buf = Buffer.alloc(84 + nFaces * 50, 0);
  buf.write("Binary STL test", 0, "ascii");
  buf.writeUInt32LE(nFaces, 80);
  for (let i = 0; i < nFaces; i++) {
    const off = 84 + i * 50;
    buf.writeFloatLE(1.0, off);
    for (let j = 1; j < 12; j++) buf.writeFloatLE(0.0, off + j * 4);
  }
  return buf;
}

function makePlyBinary() {
  const header = Buffer.from(
    "ply\nformat binary_little_endian 1.0\nelement vertex 3\nproperty float x\nproperty float y\nproperty float z\nelement face 1\nproperty list uchar int vertex_indices\nend_header\n",
    "ascii"
  );
  const verts = Buffer.alloc(3 * 12);
  verts.writeFloatLE(0.0, 0);  verts.writeFloatLE(0.0, 4);  verts.writeFloatLE(0.0, 8);
  verts.writeFloatLE(1.0, 12); verts.writeFloatLE(0.0, 16); verts.writeFloatLE(0.0, 20);
  verts.writeFloatLE(0.5, 24); verts.writeFloatLE(1.0, 28); verts.writeFloatLE(0.0, 32);
  const face = Buffer.alloc(1 + 3 * 4);
  face.writeUInt8(3, 0);
  face.writeInt32LE(0, 1); face.writeInt32LE(1, 5); face.writeInt32LE(2, 9);
  return Buffer.concat([header, verts, face]);
}

