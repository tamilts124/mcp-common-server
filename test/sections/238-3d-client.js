// ── Test harness ──────────────────────────────────────────────────────────────
let passed = 0, failed = 0;
const errors = [];

function test(name, fn) {
  try {
    fn();
    passed++;
    process.stdout.write(`  ✓ ${name}\n`);
  } catch (e) {
    failed++;
    errors.push({ name, error: e.message });
    process.stdout.write(`  ✗ ${name}: ${e.message}\n`);
  }
}

function assert(condition, msg) {
  if (!condition) throw new Error(msg || "Assertion failed");
}

function assertEqual(a, b, msg) {
  if (a !== b) throw new Error(msg || `Expected ${JSON.stringify(a)} === ${JSON.stringify(b)}`);
}

function assertThrows(fn, pattern) {
  let threw = false;
  try { fn(); } catch (e) {
    threw = true;
    if (pattern && !e.message.includes(pattern))
      throw new Error(`Expected error matching '${pattern}', got: ${e.message}`);
  }
  if (!threw) throw new Error("Expected function to throw");
}

// ── Temp dir + file generators ────────────────────────────────────────────────
const TMP = fs.mkdtempSync(path.join(os.tmpdir(), "3d-client-tests-"));

function write(name, content) {
  const p = path.join(TMP, name);
  fs.writeFileSync(p, content);
  return p;
}
function writeBuf(name, buf) {
  const p = path.join(TMP, name);
  fs.writeFileSync(p, buf);
  return p;
}

